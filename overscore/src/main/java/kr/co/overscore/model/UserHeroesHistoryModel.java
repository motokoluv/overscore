package kr.co.overscore.model;

public class UserHeroesHistoryModel {
	String historyGroupId;
	String heroesId;
	String statId;
	String value;

	public String getHistoryGroupId() {
		return historyGroupId;
	}

	public void setHistoryGroupId(String historyGroupId) {
		this.historyGroupId = historyGroupId;
	}

	public String getHeroesId() {
		return heroesId;
	}

	public void setHeroesId(String heroesId) {
		this.heroesId = heroesId;
	}

	public String getStatId() {
		return statId;
	}

	public void setStatId(String statId) {
		this.statId = statId;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
